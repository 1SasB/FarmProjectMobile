class Product {
  String name;
  double ros;
  String startDate;
  String endDate;
  String image;
  String description;
  String status;
  double costPerIncome;

  Product({
    required this.name,
    required this.ros,
    required this.costPerIncome,
    required this.description,
    required this.endDate,
    required this.image,
    required this.startDate,
    required this.status,
  });
}
